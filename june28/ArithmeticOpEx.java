package june28;

public class ArithmeticOpEx 
{
	public static void main(String[] args) 
	{
	int num1 = 10;
	int num2  = 5;
	
	int add,sub,div,mul,mod;
	
	add = num1+num2;
	div = num1/num2;
	mul = num1*num2;
	sub = num1-num2;
	mod = num1%num2;
	
	System.out.println("Addition of two value is : "+add);
	System.out.println("Division of two value is :  "+div);
	System.out.println("Multiplication of two value is :  "+mul);
	System.out.println("Substraction of two value is : "+sub);
	System.out.println("Mudulus of two value is :  "+mod);
	
	
	}

}
